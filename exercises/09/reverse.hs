{-
 - линеен рекурсивен процес - n на брой конкатенации, където n е дължината на
 - списъка => O(n^2)
 -}
reverse' :: [t] -> [t]
reverse' [] = []
reverse' (x:xs) = reverse xs ++ [x]

-- линеен итеративен процес - O(n), където n е дължината на списъка
reverse'' :: [t] -> [t]
reverse'' = foldl (flip (:)) []
